                        <p align=center><a href="http://www.bhm.ba/">BHM Zenica 
                          </a></p>
                        <p>BHM Zenica je privatno preduzece koje posluje od 1989. 
                          godine u obalasti primjene i koristenja racunara u razlicitim 
                          oblastima. Osnivaci i saradnici BHM imaju dugogodisnje 
                          iskustvo u primjeni racunara od automatizacije industrijskih 
                          procesa, preko projektovanja elektronskih modula pomocu 
                          racunara, do izrade racunarskih programa za posebne 
                          namjene.</p>
                        <p>Sve vecim prodorom racunara u mnoge zenicke firme, 
                          BHM pocinje da se bavi edukacijom u oblasti koristenja 
                          racunara u razlicitim poslovima. U saradnji sa Soros 
                          Fondacijom osnovali su racunarsku skolu 1994. godine 
                          i pruzili zadovoljstvo stotinama ucenika zenickih skola 
                          da u najtezim trenucima upoznaju svijet racunara i da 
                          se, bar po tome, osjecaju jednakim svojim vrsnjacima 
                          sirom svijeta.</p>
                        <p>U oblasti racunarske edukacije BHM ima bogato iskustvo 
                          u obracunavanju razlicitog uzrasta, od najmladjih (11 
                          do 14 godina), preko skolske omladine i studenata, do 
                          odraslih polaznika. Za svaki od ovih uzrasta imaju odgovarajuci 
                          pristup obucavanju, kojeg stalno unapredjuju i usavrsavaju. 
                          Njihova racunarska edukacija je zasnovana na siroko 
                          rasprotranjenim programima. Koristimo najnovije verzije 
                          tih programa.</p>
                        <p>Organizovali su i razlicite vrste specijalistickih 
                          obuka potrebnih za razlicita zanimanja, od racunarske 
                          opreme za stamparije, preko standardnih uredskih poslova, 
                          do dizajnerskih i projektantskih poslova. </p>
                        <p>Njihove najznacajnije reference u ovoj oblasti su obuka 
                          radnika:<br>
                          Skupstine opcine Zenica, Medjunarodnog komiteta crvenog 
                          kriza, fabrike UNICO iz Tesnja, Elektrodistribucije 
                          Zenica, Zavoda za platni promet Zenica, Poreske uprave 
                          Zenica, Kantonalne bolnice Zenica, grupe ratnih vojnih 
                          invalida, grupe demobilisanih pripadnika Armije BiH 
                          itd.</p>
                        <p>Program obuke su po sadrzaju prilagodili savremenom 
                          evropskom standardu. Naime, obuku izvode prema ECDL 
                          (European Computer Driving Licence) standardu, koji 
                          ce vjerovatno biti standard i u nasoj zemlji. Ovaj standard 
                          omogucava da vjestina rada na racunaru i certifikat 
                          za to znanje vrijede u svim zemljama Evrope. </p>
