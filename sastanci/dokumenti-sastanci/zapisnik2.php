                  <div class="Section1">
                    <font size="5"><em><strong>Zapisnik sa sastanka Udruzenja 
                    Linux Korisnika Zenice</strong></em></font><br>
                    <strong><em>odrzanog 1.06.2003. godine</em></strong><div class="MsoNormal" style="text-align: center" align="center">
                      <hr align="center" width="100%" SIZE="2">
                      <div align="left">
                        Dana 1.06.2003. godine (nedelja) odrzan je drugi redovni 
                        sastanak Udruzenja. Sastanak je u potpunosti uspio, iako 
                        neki clanovi nisu mogli doci. Na dnevnom redu sastanka 
                        nasao se sljedeci plan:
                      </div>
                      <p align="left">- Otvorena rasprava na polju odnosa sa 
                      Udruzenjem Linux Korisnika Bosne i Hercegovine.<br>
                      - Rasprava o projektu provjere pravopisa<br>
                      - Razmatranje prijedloga o prevodu Linux SuSE 
                      dokumentacije<br>
                      - Testiranje AX25 mreze (LAN)</p>
                      <p align="left">Prilikom rasprave o odnosu sa Udruzenjem 
                      Linux Korisnika Bosne i Hercegovine kljucne tacke bile su 
                      forum, domena, clanarine i naziv Udruzenja. Clanovi su se 
                      slozili da naziv naseg Udruzenja i dalje ostaje Udruzenje 
                      Linux Korisnika Zenice, a ne Udruzenje Linux Korisnika 
                      Bosne i Hercegovine / Distributivni centar Zenica. Pored 
                      toga zahtjev da se forum naseg Udruzenja prebaci na 
                      http://forum.linux.org.ba (sve poruke bile bi prebacene na 
                      forum ULK-a) je odbijen, jer forum uglavnom djeluje 
                      interno, te je jeddno od glavnih sredstava komunikacije 
                      Udruzenja. Sto se tice domene, ona i dalje ostaje 
                      http://www.lugze.org, sa tim da ce domena 
                      http://ze.lugbih.org i dalje ostati aktivna. Sistem 
                      placanja clanarina ostaje isti kao i onaj dogovoreni na 
                      prvom sastanku Udruzenja (pogledati zapisnik sa prvog 
                      sastanka). U narednom periodu Mesa Sacirovic, Nedim 
                      Hadzimahmutovic i Sanjta Kemal ce otici u Sarajevo, u 
                      prostorije ULK-a, da detaljno dogovore ove stavke.</p>
                      <p align="left">Na sastanku se vodila i rasprava o 
                      projektu provjere pravopisa. Ovaj projekat kordinira Nedim 
                      Hadzimahmutovic. Dogovoreno je da ce se raditi po 
                      pravopisu Senahida Halilovica (iz 1995. godine). Pravopis 
                      ce biti skeniran, detaljno pregledan, te na osnovu toga ce 
                      biti zavrsen projekat provjere pravopisa. Ukoliko projekat 
                      ne uspije sa skeniranjem, dogovoreno je da se radi 
                      prekucavanje cijelog pravopisa, a pritom ce svaki clan 
                      Udruzenja dobiti zadatak koji odredi kordinator.</p>
                      <p align="left">Na sastanku je razmatran prijedlog da se 
                      pokrene projekat prevodjenja SuSE Linux dokumentacije. 
                      Denis Radovanovic je preuzeo obavezu slanja maila ljudima 
                      iz SuSE-a koji su u mogucnosti da nam daju sve sto je 
                      potrebno za ovaj projekat (dozvole, literatura i sl.). 
                      Ovaj projekat ce biti detaljnije razmotren na nekom od 
                      narednih sastanaka Udruzenja.</p>
                      <p align="left">Dejan Menges i Denis Radovanovic dosli su 
                      sa prijedlogom testiranja AX25 mreze (LAN-a). Ovaj 
                      projekat je u potpunosti prihvacen, sa tim da ce 
                      detaljnije biti razmotren na nekom od narednih sastanaka 
                      Udruzenja.</p>
                      <p align="left">Sljedeci sastanak Udruzenja Linux 
                      Korisnika Zenice zakazan je za 15.06.2003. godine.</p>
